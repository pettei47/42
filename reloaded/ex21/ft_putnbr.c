#include <unistd.h>

void	ft_putnbr(int nb)
{
	long put;

	put = nb;
	if (put < 0)
	{
		write(1, "-", 1);
		put = -1 * put;
	}
	if (put > 9)
		ft_putnbr(put / 10);
	write(1, &"0123456789"[put % 10], 1);
}
